												<h5>Speaking( 11–14 minutes)</h5>
												<div class="cmt-horizontal_sep width-100 mt-10 mb-10"></div>
												<div class="featured-desc">
													<p class="coaching-clr">The Speaking component assesses your use of spoken English, and takes between 11 and 14 minutes to complete. Every test is recorded. The Speaking component is delivered in such a way that it does not allow people to rehearse set responses beforehand.</p>
													<h5>Part  1</h5>
													<p class="coaching-clr">The examiner will ask you general questions about yourself and a range of familiar topics, such as home, family, work, studies and interests. This part lasts between 4 and 5 minutes.</p>
													<h5>Part  2</h5>
													<p class="coaching-clr">You will be given a card which asks you to talk about a particular topic. You will have 1 minute to prepare before speaking for up to 2 minutes. The examiner will then ask one or two questions on the same topic to finish this part of the test.</p>
													<h5>Part  3</h5>
													<p class="coaching-clr">You will be asked further questions connected to the topic in Part 2. These questions will give you the opportunity to discuss more abstract ideas and issues. This part of the test lasts between 4 and 5 minutes.</p>
