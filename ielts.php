									<h4>IELTS</h4>
									<h5>What is IELTS?</h5>
									<p class="coaching-clr">International English Language Testing System or IELTS is a globally accepted test to check English communication skills of students pursuing higher studies abroad. IELTS is jointly managed by British Council, IDP: IELTS Australia and the University of Cambridge ESOL Examinations (Cambridge ESOL) and is delivered through more than 500 locations in over 135 countries. IELTS is recognized by more than 6000 institutions, government departments, professional associations and other organizations.</p>
									<div class="row mt-30">
										<div class="col-lg-12">
											<h4>IELTS TRAINING COURSE</h4>
											<p class="coaching-clr">The IELTS test is designed to test your abilities in all four skills – listening, reading, writing and speaking in less than 2 hours 45 minutes.</p>
											<p class="coaching-clr">IELTS is available in two test formats: Academic or General Training. There are four parts – Listening, Reading, Writing and Speaking. The total test time is 2 hours and 45 minutes. All test takers take the same Listening and Speaking tests but different Reading and Writing tests. The distinction between IELTS Academic and IELTS General Training lies in the subject matter of the Reading and Writing components. Listening, Reading and Writing must be completed on the same day, with no breaks in between them. The order in which these tests are taken may vary. The Speaking test will either be after a break on the same day as the other three tests, or up to a week before or after the other tests. This will depend on your test centre.</p>
