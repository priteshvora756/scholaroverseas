												<h5>Academic Writing (60 minutes)</h5>
												<div class="cmt-horizontal_sep width-100 mt-10 mb-10"></div>
												<div class="featured-desc">
													<p class="coaching-clr">The Writing component of IELTS Academic includes two tasks. Topics are of general interest and suitable for test takers entering undergraduate and postgraduate studies or seeking professional registration.</p>
													<h5>Task 1</h5>
													<p class="coaching-clr">You will be presented with a graph, table, chart or diagram and asked to describe, summarize or explain the information in your own words. You may be asked to describe and explain data, describe the stages of a process, how something works or describe an object or event.</p>
													<h5>Task 2</h5>
													<p class="coaching-clr">You will be asked to write an essay in response to a point of view, argument or problem. Responses to both tasks must be in a formal style.</p>
												</div>
