									<h5>What is TOEFL?</h5>
									<p class="coaching-clr">A standardized test, the Test Of English as a Foreign Language (TOEFL) assesses the English proficiency of applicants, whose native language is not English. It tests the ability to understand North American English. Most educational institutes mostly in the US and Canada (approximately 2400) ask applicants for their TOEFL Scores. Most applicants take the TOEFL test as a prerequisite for admission into colleges and universities where English is used or required. In addition, many government, licensing, and certification agencies and exchange and scholarship programs use TOEFL scores to evaluate the English proficiency of people for whom English is not their native language.</p>
